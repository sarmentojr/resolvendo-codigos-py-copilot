# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!

info_nome = input("Qual o seu nome?")
info_sobrenome = input("Qual o seu sobrenome?")

concatenando_nome_e_sobrenome = info_nome + " " + info_sobrenome

print("O seu nome completo é ", concatenando_nome_e_sobrenome)